    public function enableMember($id)
    {
        $member = Member::find($id);
        $member->status = 1;
        $member->save();
        return redirect('/ratnapustakalaya/members')->with('success', 'Member  Enabled Successfully');
    }

    public function disableMember($id)
    {
        $member = Member::find($id);
        $member->status = 0;
